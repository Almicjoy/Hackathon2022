class Patient {
    constructor (fname, lname, id, status) {
        this.fname = fname
        this.lname = lname
        this.id = id
        this.status = status
    }

    get fname () {
        return this.fname
    }
    get lname () {
        return this.lname
    }
    get id () {
        return this.id
    }
    get status () {
        return this.status
    }
    

}

